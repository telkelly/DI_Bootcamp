# What is a class?
# A class is a template for creating objects (instances). It defines the properties (attributes) and behaviors (methods) that objects of that class will have. Classes serve as a way to define the structure and behavior of objects in object-oriented programming.
#
# What is an instance?
# An instance is a specific realization or occurrence of a class, created based on the blueprint provided by the class. It is an individual object with its own set of attributes and methods, distinct from other instances of the same class.
#
# What is encapsulation?
# Encapsulation is one of the fundamental principles of object-oriented programming. It refers to the bundling of data (attributes) and the methods (functions) that operate on that data into a single unit, known as a class. Encapsulation helps to control access to the internal state of an object and prevents direct manipulation of its data.
#
# What is abstraction?
# Abstraction is the process of simplifying complex reality by modeling classes based on the essential properties and behaviors of objects, while ignoring unnecessary details. It allows you to create a clear and conceptual representation of an object in code.
#
# What is inheritance?
# Inheritance is a mechanism in object-oriented programming that allows a new class (subclass or derived class) to inherit properties and behaviors (attributes and methods) from an existing class (superclass or base class). This promotes code reuse and the creation of specialized classes that enhance or override the functionality of the base class.
#
# What is multiple inheritance?
# Multiple inheritance is a feature of some object-oriented programming languages that allows a class to inherit attributes and methods from more than one superclass. This enables a subclass to combine features from multiple parent classes. However, multiple inheritance can lead to complexity and conflicts if not managed properly.
#
# What is polymorphism?
# Polymorphism is the ability of different classes to be treated as instances of a common superclass through a shared interface. It allows objects of different classes to be used interchangeably when they share common methods or behaviors, even though they might have different implementations.
#
# What is method resolution order or MRO?
# Method Resolution Order (MRO) is the order in which a programming language resolves method calls in classes with multiple inheritance. In languages like Python, the C3 Linearization algorithm is used to determine the sequence in which the inherited methods are called, ensuring that the appropriate methods are called in the right order while handling multiple inheritance scenarios.
